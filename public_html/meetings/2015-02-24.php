<u>Introductory topic:</u>
</br>
<b>LDAP: The little black book for your network </b> 
<br>Jeff Deroshia</br>
</br>
If you have more than one computer in your home or office network, you can save at least half the time administering the computers by centralizing user accounts/passwords, home directories and other network mounts, as well as host information in a single database. Using LDAP, you can create/update accounts, etc. in a single location and have it distributed to all the computers in your network. In this introductory talk, I will discuss what LDAP is and how it can be used to simplify your life, at least regarding system administration. 
</br>
<br></br>
<u>Advanced topic:</u>
</br>
<b>Using LDAP to centralize the management of user accounts and hosts on your network</b> 
<br>Jeff Deroshia</br>
</br>
If you have to manage more than one computer in a network environment, the simplest task, such as adding a user or changing a user's password, can quickly become a hassle if it needs to be repeated for every machine. Your best option is to store account and host information in a directory service that is accessible to all the machines on your network. Enter the Lightweight Directory Access Protocol, or LDAP. In this talk I will discuss setting up an LDAP server as the central repository for your account/host information as well as configuring your networked machines to get their account/host information from the ldap server. 
