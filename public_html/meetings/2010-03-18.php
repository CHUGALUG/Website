<h2>Configuration Management Systems</h2>
<p>Date: March 18th, 2010</p>

<p>Brian Whigham and Nage Childers gave an excellent presentation describing <a href="http://www.cfengine.org/">CFEngine</a> and <a href="http://reductivelabs.com/products/">Puppet</a> as well as configuration management systems in general.  They use both products to administer systems for UGA.</p>

<p>CFEngine is the older of the two systems and sounds as if it does an acceptable job (although version 3 is now available which likely has improvements).  Puppet is generally more modern. It is written in Ruby and is very extensible with an active community. Other products are available including Fabric, Bcfg2, Chef, and <a href="http://en.wikipedia.org/wiki/Comparison_of_open_source_configuration_management_software">many others</a></p>

<p>The demonstrated some sample configurations that helped to make some of the concepts more concrete.</p>

<p>Their presentation is available <a href="/http://docs.google.com/present/view?id=dgw7kx89_91f9m6d5gs">Here</a></p>

