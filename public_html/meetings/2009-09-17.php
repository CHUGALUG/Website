<h2>Sphinx Documentation</h2>
<p>Date: September 17th, 2009</p>

<p><a href="http://www.doughellmann.com/">Doug Hellmann</a> gave an excellent presentation on the <a href="http://sphinx.pocoo.org/">Sphinx Documentation Generator</a>.  The project is written in Python and its original use was to document Python and itself.</p>

<p>Sphinx takes input files in a reStructuredText format, which is (yet another) text markup language.  It can output documentation in HTML and PDF formats.    </p>

<p>Doug's presentation (created with Sphinx) is available <a href="http://www.doughellmann.com/docs/chugalug-sphinx-intro/">here</a></p>

