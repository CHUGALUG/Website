<h2>Ansible</h2>
<p>Date: Jan. 17, 2013</p>

<p>Derek Carter gave a great presentation about <a href="http://ansible.cc/">Ansible</a>!</p>

<blockquote>
Systems automation (also known as configuration management) is a hot topic today with various different solutions being presented: Chef, Puppet, CFEngine, and others.
<br></br>
The problem with most configuration management systems is complexity.
<br></br>
What if you could manage your systems without installing a client program, managing a complex PKI signature system, and learning an esoteric programming language? While only using daemons and access methods you're already using?
<br></br>
You can, with <a href="http://ansible.cc/">Ansible</a>. Ansible is a simple configuration management system written in Python, which uses SSH as a communication channel.
<br></br>
Come learn how quick and easy the YAML style syntax can be used to define the state of your systems.
<br></br>
Also, learn how easy Ansible is to extend. If you can write a shell script, you can make Ansible do almost anything.
<br></br>
