<h2>Preseeding the Debian Installer</h2>
<h2>By <a href="http://polibyte.com/resume">Brian Pitts</a></h2>
<p>Date: April 16th, 2009</p>

<p>Brian presented on his experience with preseeding the Debian installer. He
has used this while preparing hundreds of laptops with 
<a href="http://freeitathens.org">Free IT Athens</a>.</p>

<p>The Debian installer is able to read answers to virtually any question it 
asks from a preseed configuration file.  He detailed how to create the file,
including how to install a package interactively and then to read your 
answers from the debconf database.  Various methods were demonstrated for
starting the installer and specifying the preseed file.

<p>His presentation is available <a href="http://www.scribd.com/doc/14313466/Preseeding-the-Debian-Installer">here</a></p>

