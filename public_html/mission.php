<h3>Mission</h3>
<p>
CHUGALUG is a very informal group with no offical membership, officers,
rules, etc.  Its main purpose is to bring together people who dig Linux.
Once together, they can choose what to do, but typically it might include
talking about Linux, helping others with Linux problems or projects, and 
socializing with each other.</p>

<h3>Why so informal?  There are really no rules?</h3>
<p>CHUGALUG has never had any formal rules and most of us agree that we
don't need any.<p>

<p>The thinking is that we would rather spend our time enjoying Linux
   and each other's company than fussing with a lot of rules, administrative
   overhead and politics.</p>



