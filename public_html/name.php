earliest members of CHUGALUG.  It pays homage to the fact that the 
father of Linux, Linus Torvalds, has an affection for beer.</p>

<p>Drinking or a love of beer are <b>not</b> required to: </p>

a) run Linux 
or b) attend CHUGALUG meetings
